#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    
    return self.searchMapList.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc]initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:@""];
        cell.textLabel.textColor = MainColor;
        cell.detailTextLabel.textColor = SecondTitleColor;
    }
    
    [self configCell:cell andRowIndexPath:indexPath];
    
    
    return cell;
}


-(CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath
{
    return 60;
}


-(void)configCell:(UITableViewCell *)cell andRowIndexPath:(NSIndexPath *)indexPath
{
    AMapPOI *obj = self.searchMapList[indexPath.row];
    
    
    cell.textLabel.text = obj.name;
    
    cell.detailTextLabel.text = [obj.province stringByAppendingFormat:@"%@%@%@",obj.province,obj.city,obj.address];
    
    
    
    
}


